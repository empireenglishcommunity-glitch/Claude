const INDEX: Record<string, WordEntry> = OFFLINE_WORDS.reduce(
  (acc, w) => {
    acc[w.word.toLowerCase()] = { ...w, source: 'offline' };
    return acc;
  },
  {} as Record<string, WordEntry>,
);

export function findOfflineWord(query: string): WordEntry | undefined {
  return INDEX[query.trim().toLowerCase()];
}

export function searchOfflineWords(query: string, limit = 8): WordEntry[] {
  const q = query.trim().toLowerCase();
  if (!q) return OFFLINE_WORDS.slice(0, limit);
  return OFFLINE_WORDS.filter((w) => w.word.toLowerCase().includes(q)).slice(0, limit);
}

export function wordOfTheDay(): WordEntry {
  // Deterministic per calendar day so it feels intentional, works offline.
  const start = new Date(2024, 0, 1).getTime();
  const today = new Date();
  const dayIndex = Math.floor((today.getTime() - start) / 86_400_000);
  const entry = OFFLINE_WORDS[((dayIndex % OFFLINE_WORDS.length) + OFFLINE_WORDS.length) % OFFLINE_WORDS.length];
  return { ...entry, source: 'offline' };
}
